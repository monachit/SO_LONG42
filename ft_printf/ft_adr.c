#include "ft_printf.h"

int	ft_adr(void *d)
{
	int	i;

	i = 0;
	if (d == 0)
	{
		write(1, "(nil)", 5);
		return (5);
	}
	i += ft_putstr("0x");
	i += ft_hex((unsigned long)d);
	return (i);
}
